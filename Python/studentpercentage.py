#program to substitue grade to students based on their percentage.
percentage = float(input("Please enter your percentage: \n"))
if(percentage>=90):
    print("Grade A")
elif(percentage>=80):
    print("Grade B")
elif(percentage>=70):
    print("Grade C")
elif(percentage>=60):
    print("Grade E")
else:
    print("Grade E")
